package com.jacktan.benchmark.dto;

public class Result {
    private String name;
    private int age;

    public Result() {
    }

    public Result(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
